package eca.samples.buildingblocks.pt1;

public class MathComparisons {

    public static void main(String[] args) {

        var lessThan = 1 < 2;
        System.out.println(lessThan);

        var greaterThan = 1 > 2;
        System.out.println(greaterThan);

        var lessThanOrEqual = 1 <= 2;
        System.out.println(lessThanOrEqual);

        var greaterThanOrEqual = 1 >= 2;
        System.out.println(greaterThanOrEqual);

        var equal = 1 == 2; // note it is double equals signs
        System.out.println(equal);

    }

}