package eca.answers.lesson2;

public class MathComparisons {

    public static void main(String[] args) {
        /*
         * Create 2 variables, both of type Integer:
         * - a with the value 5
         * - b with the value 6
         */
        Integer a = 5;
        Integer b = 6;

        // System.out.println prints the result of the expression that is passed to it.
        System.out.println(a == b); // equals
        System.out.println(a < b); // less than
        System.out.println(a <= b); // less than or equal
        System.out.println(a > b); // greater
        System.out.println(a >= b); // greater than or equal
    }
}
