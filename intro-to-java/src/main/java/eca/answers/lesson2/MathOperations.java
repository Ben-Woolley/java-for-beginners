package eca.answers.lesson2;

public class MathOperations {

    public static void main(String[] args) {
        /*
         * Create 2 variables, both of type Integer:
         * - a with the value 5
         * - b with the value 12
         */
        Integer a = 5;
        Integer b = 12;

        // System.out.println prints the result of the expression that is passed to it.
        System.out.println(a + b); // addition
        System.out.println(a - b); // subtraction
        System.out.println(a * b); // multiplication
        System.out.println(a / b); // division
        System.out.println(a % b); // modulus (remainder of integer division)
    }
}
